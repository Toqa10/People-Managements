# --- Helper function to save plots ---
def fig_to_image(fig):
    """Converts a matplotlib figure to a PNG image in bytes."""
    buf = io.BytesIO()
    fig.savefig(buf, format="png", bbox_inches='tight', dpi=300)
    buf.seek(0)
    return buf

